package com.seintes.wanandroid.project;

import java.io.Serializable;
import java.util.List;

/**
 * @Description 体系页面体系栏中的数据
 * @Author xmhaz
 * @Time 2023/12/12 13:28
 */
public class Tab implements Serializable {
    private List<Tab> children;
    private int couserId;
    private int id;
    private String name;
    private int order;
    private int parentChapterId;

    public List<Tab> getChildren() {
        return children;
    }

    public void setChildren(List<Tab> children) {
        this.children = children;
    }

    public int getCouserId() {
        return couserId;
    }

    public void setCouserId(int couserId) {
        this.couserId = couserId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getOrder() {
        return order;
    }

    public void setOrder(int order) {
        this.order = order;
    }

    public int getParentChapterId() {
        return parentChapterId;
    }

    public void setParentChapterId(int parentChapterId) {
        this.parentChapterId = parentChapterId;
    }

    @Override
    public String toString() {
        return "Tab{" +
                "children=" + children +
                ", couserId=" + couserId +
                ", id=" + id +
                ", name='" + name + '\'' +
                ", order=" + order +
                ", parentChapterId=" + parentChapterId +
                '}';
    }
}
